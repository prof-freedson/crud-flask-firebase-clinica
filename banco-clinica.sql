CREATE DATABASE banco_clinica;

USE banco_clinica;

-- Criação da tabela de endereços
CREATE TABLE endereco (
    id_end INT AUTO_INCREMENT PRIMARY KEY,
    rua VARCHAR(100),
    numero VARCHAR(10),
    bairro VARCHAR(50),
    cidade VARCHAR(50),
    estado CHAR(2)
);

-- Tabela de pacientes
CREATE TABLE paciente (
    id_pac INT AUTO_INCREMENT PRIMARY KEY,
    nome_pac VARCHAR(100),
    data_nasc_pac DATE,
    peso_pac DECIMAL(5,2),
    alt_pac DECIMAL(4,2),
    tipo_sang VARCHAR(3),
    id_end INT,
    FOREIGN KEY (id_end) REFERENCES endereco(id_end) ON DELETE SET NULL
);

-- Tabela de médicos
CREATE TABLE medico (
    id_med INT AUTO_INCREMENT PRIMARY KEY,
    nome_med VARCHAR(100),
    data_nasc_med DATE,
    espec_med VARCHAR(100),
    id_end INT,
    FOREIGN KEY (id_end) REFERENCES endereco(id_end) ON DELETE SET NULL
);

-- Telefones dos pacientes
CREATE TABLE telefone_paciente (
    id_tel_pac INT AUTO_INCREMENT PRIMARY KEY,
    id_pac INT,
    num_tel VARCHAR(15),
    FOREIGN KEY (id_pac) REFERENCES paciente(id_pac) ON DELETE CASCADE
);

-- Telefones dos médicos
CREATE TABLE telefone_medico (
    id_tel_med INT AUTO_INCREMENT PRIMARY KEY,
    id_med INT,
    num_tel VARCHAR(15),
    FOREIGN KEY (id_med) REFERENCES medico(id_med) ON DELETE CASCADE
);

-- E-mails dos pacientes
CREATE TABLE email_paciente (
    id_email_pac INT AUTO_INCREMENT PRIMARY KEY,
    id_pac INT,
    end_email VARCHAR(100),
    FOREIGN KEY (id_pac) REFERENCES paciente(id_pac) ON DELETE CASCADE
);

-- E-mails dos médicos
CREATE TABLE email_medico (
    id_email_med INT AUTO_INCREMENT PRIMARY KEY,
    id_med INT,
    end_email VARCHAR(100),
    FOREIGN KEY (id_med) REFERENCES medico(id_med) ON DELETE CASCADE
);

-- Tabela de consultas
CREATE TABLE consulta (
    id_cons INT AUTO_INCREMENT PRIMARY KEY,
    id_pac INT,
    id_med INT,
    data_cons DATE,
    inic_cons TIME,
    term_cons TIME,
    FOREIGN KEY (id_pac) REFERENCES paciente(id_pac) ON DELETE CASCADE,
    FOREIGN KEY (id_med) REFERENCES medico(id_med) ON DELETE CASCADE
);

-- Tabela de receitas
CREATE TABLE receita (
    id_rec INT AUTO_INCREMENT PRIMARY KEY,
    id_cons INT,
    observacoes TEXT,
    FOREIGN KEY (id_cons) REFERENCES consulta(id_cons) ON DELETE CASCADE
);

-- Tabela de medicamentos
CREATE TABLE medicamento (
    id_medic INT AUTO_INCREMENT PRIMARY KEY,
    nome_medic VARCHAR(100),
    fabric_medic VARCHAR(100),
    posol_medic VARCHAR(100)
);

